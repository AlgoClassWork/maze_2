from pygame import *

# =========================================================
# НАСТРОЙКИ ОКНА
# =========================================================

WINDOW_WIDTH = 700
WINDOW_HEIGHT = 500
FPS = 60

# Размер спрайтов
SPRITE_WIDTH = 50
SPRITE_HEIGHT = 50

# Скорость игрока
PLAYER_SPEED = 10

# =========================================================
# СОЗДАНИЕ ОКНА И ИНИЦИАЛИЗАЦИЯ
# =========================================================

window = display.set_mode((WINDOW_WIDTH, WINDOW_HEIGHT))
display.set_caption("Лабиринт")

# =========================================================
# ЗАГРУЗКА ФОНА
# =========================================================

background = image.load("background.jpg")
background = transform.scale(background, (WINDOW_WIDTH, WINDOW_HEIGHT))

# =========================================================
# БАЗОВЫЙ КЛАСС СПРАЙТА
# =========================================================

class GameSprite(sprite.Sprite):
    """
    Базовый класс для всех игровых объектов.
    
    Отвечает за:
    - загрузку изображения
    - хранение координат
    - отображение на экране
    """

    def __init__(self, img_path, x, y):
        """
        Конструктор спрайта.

        img_path — путь к изображению
        x, y — стартовые координаты
        """

        # Загружаем изображение
        self.image = image.load(img_path)

        # Изменяем размер изображения
        self.image = transform.scale(
            self.image,
            (SPRITE_WIDTH, SPRITE_HEIGHT)
        )

        self.rect = self.image.get_rect()

        # Координаты объекта
        self.rect.x = x
        self.rect.y = y

    def draw(self):
        """
        Отрисовывает объект на экране.
        """

        window.blit(self.image, (self.rect.x, self.rect.y))


# =========================================================
# КЛАСС ИГРОКА
# =========================================================

class Player(GameSprite):
    """
    Класс игрока.

    Наследуется от GameSprite
    и добавляет управление клавиатурой.
    """

    def move(self):
        """
        Движение игрока по клавишам:
        W — вверх
        S — вниз
        A — влево
        D — вправо
        """

        keys = key.get_pressed()

        # Движение вверх
        if keys[K_w] and self.rect.y > 0:
            self.rect.y -= PLAYER_SPEED

        # Движение вниз
        if keys[K_s] and self.rect.y < WINDOW_HEIGHT - SPRITE_HEIGHT:
            self.rect.y += PLAYER_SPEED

        # Движение влево
        if keys[K_a] and self.rect.x > 0:
            self.rect.x -= PLAYER_SPEED

        # Движение вправо
        if keys[K_d] and self.rect.x < WINDOW_WIDTH - SPRITE_WIDTH:
            self.rect.x += PLAYER_SPEED


# =========================================================
# КЛАСС ВРАГА
# =========================================================

class Enemy(GameSprite):
    def move(self):
        if self.rect.x <= 450:
            self.side = 'right'
        if self.rect.x >= 650:
            self.side = 'left'

        if self.side == 'left':
            self.rect.x -= 5
        else:
            self.rect.x += 5

# =========================================================
# КЛАСС ВРАГА
# =========================================================

class Wall(sprite.Sprite):
    def __init__(self, x, y, width, height):
        self.image = Surface((width, height))
        self.image.fill((0, 255, 0))
        self.rect = self.image.get_rect()
        self.rect.x = x
        self.rect.y = y

    def draw(self):
        window.blit(self.image, (self.rect.x, self.rect.y))
    

# =========================================================
# СОЗДАНИЕ ОБЪЕКТОВ
# =========================================================

# Игрок
hero = Player("hero.png", 0, 450)

# Враг
enemy = Enemy("cyborg.png", 650, 300)

# Цель
goal = GameSprite("treasure.png", 650, 450)

# Стены
wall1 = Wall(100, 100, 10, 400)
wall2 = Wall(100, 100, 500, 10)

# =========================================================
# ИГРОВОЙ ТАЙМЕР
# =========================================================

clock = time.Clock()

# =========================================================
# ГЛАВНЫЙ ИГРОВОЙ ЦИКЛ
# =========================================================

game = True

while game:

    # -----------------------------------------------------
    # ОБРАБОТКА СОБЫТИЙ
    # -----------------------------------------------------

    for some_event in event.get():

        # Закрытие окна
        if some_event.type == QUIT:
            game = False

    # -----------------------------------------------------
    # ОТРИСОВКА ФОНА
    # -----------------------------------------------------

    window.blit(background, (0, 0))

    # -----------------------------------------------------
    # ЛОГИКА ИГРЫ
    # -----------------------------------------------------

    # Движение игрока
    hero.move()

    # Движение врага
    enemy.move()

    # -----------------------------------------------------
    # ОТРИСОВКА ОБЪЕКТОВ
    # -----------------------------------------------------

    hero.draw()
    enemy.draw()
    goal.draw()

    wall1.draw()
    wall2.draw()

    # Условие поражения
    if sprite.collide_rect(hero, enemy):
        print('Ты проиграл')

    # Условие победы
    if sprite.collide_rect(hero, goal):
        print('Ты победил')

    # Условие столкновение со стеной
    if sprite.collide_rect(hero, wall1):
        hero.rect.x = 0
        hero.rect.y = 450

    # -----------------------------------------------------
    # ОБНОВЛЕНИЕ ЭКРАНА
    # -----------------------------------------------------

    display.update()

    # Ограничение FPS
    clock.tick(FPS)

# =========================================================
# ВЫХОД ИЗ ИГРЫ
# =========================================================

quit()
